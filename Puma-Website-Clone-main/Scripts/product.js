function DisplayData(productData) {
  // document.querySelector(".product_cont").innerHTML = "";
  productData.map(function (elem) {
    var prod_container = document.querySelector(".product_cont");

    var productDiv = document.createElement("div");
    productDiv.classList.add("prod");

    var htmlData = `  <div class="image">
                <img
                  src="${elem.image}"
                  alt=""
                  class="fluid-img"
                />
              </div>
              <div class="img_desc d-flex">
                <p class="desc text-dark m-0 p-0">
                  ${elem.brand_info}
                </p>
                <div class="price_cont">
                  <p class="text-danger m-0 p-0">Rs <span>${elem.price}</span></p>
                  <p class="text-secondary m-0 p-0">Rs <span>${elem.strick_price}</span></p>
                </div>
              </div>`;

    productDiv.insertAdjacentHTML("afterbegin", htmlData);
    prod_container.append(productDiv);

    productDiv.addEventListener("click", function () {
      // addInwishlist(elem);
      localStorage.setItem("FullDetailProduct", JSON.stringify(elem));
      window.location.href = "detailProd.html";
    });
  });
}

document.querySelector("#price").addEventListener("change", handlepricesort);
function handlepricesort() {
  var selected = document.querySelector("#price").value;

  if (selected === "low") {
    productData.sort(function (a, b) {
      return Number(a.price) - Number(b.price);
    });
  }
  console.log(productData);
  if (selected === "high") {
    productData.sort(function (a, b) {
      return Number(b.price) - Number(a.price);
    });
  }
  localStorage.setItem("shoppingItems", JSON.stringify(productData));

  DisplayData(productData);
}
